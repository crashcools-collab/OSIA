  function norm(s) { return (s || '').replace(/\s+/g, ' ').trim(); }

  function apply(lang) {
    if (lang === 'fr' || !DICT[lang]) return;
    var map = DICT[lang];
    var walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null);
    var nodes = [], n;
    while ((n = walker.nextNode())) { nodes.push(n); }
    nodes.forEach(function (node) {
      var p = node.parentNode; if (!p) return;
      var pn = p.nodeName;
      if (pn === 'SCRIPT' || pn === 'STYLE' || pn === 'TEXTAREA') return;
      var raw = node.nodeValue;
      var m = raw.match(/^(\s*)([\s\S]*?)(\s*)$/);
      var core = norm(m[2]);
      if (core && map[core]) node.nodeValue = m[1] + map[core] + m[3];
    });
    document.querySelectorAll('[placeholder]').forEach(function (el) {
      var k = norm(el.getAttribute('placeholder')); if (map[k]) el.setAttribute('placeholder', map[k]);
    });
    var t = norm(document.title); if (map[t]) document.title = map[t];
    document.documentElement.lang = lang;
  }

  function makeSwitcher(lang) {
    var box = document.createElement('div'); box.className = 'osia-lang';
    ['fr', 'en', 'es'].forEach(function (l) {
      var b = document.createElement('button'); b.type = 'button'; b.textContent = l.toUpperCase();
      if (l === lang) b.className = 'on';
      b.onclick = function () { saveLang(l); location.reload(); };
      box.appendChild(b);
    });
    return box;
  }

  function makeBurger(nav) {
    var b = document.createElement('button'); b.type = 'button'; b.className = 'nav-burger';
    b.setAttribute('aria-label', 'Menu'); b.innerHTML = '&#9776;';
    b.onclick = function () { nav.classList.toggle('nav-open'); };
    return b;
  }

  function showChooser() {
    var o = document.createElement('div'); o.className = 'lang-overlay';
    o.innerHTML = '<div class="lang-box"><h2>&#127760; Langue / Language / Idioma</h2>' +
      '<p>Choisis ta langue &middot; Choose your language &middot; Elige tu idioma</p>' +
      '<div class="opts">' +
      '<button type="button" data-l="fr">&#127467;&#127479; Fran&ccedil;ais</button>' +
      '<button type="button" data-l="en">&#127468;&#127463; English</button>' +
      '<button type="button" data-l="es">&#127466;&#127480; Espa&ntilde;ol</button>' +
      '</div></div>';
    o.querySelectorAll('button').forEach(function (b) {
      b.onclick = function () { saveLang(b.getAttribute('data-l')); location.reload(); };
    });
    document.body.appendChild(o);
  }

  function init() {
    var lang = getLang();
    var nav = document.querySelector('.osia-nav');
    if (nav) {
      var right = document.createElement('div'); right.className = 'nav-right';
      right.appendChild(makeSwitcher(lang || 'fr'));
      right.appendChild(makeBurger(nav));
      nav.appendChild(right);
    }
    if (!lang) { showChooser(); return; } // 1re visite : on demande (le FR reste affiche derriere)
    apply(lang);
  }

  if (document.readyState === 'loading') document.addEventListener('DOMContentLoaded', init);
  else init();
